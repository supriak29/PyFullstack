# Example for complex datatype
## complex no.: a + bj

com1 = 1 + 3j
print(com1)
print("Datatype of com1: ", type(com1),'\n')

com2 = 8.5 - 5.6j
print(com2)
print("Datatype of com2: ", type(com2),'\n')

com3 = -90.5 + 56.47j
print(com3)
print("Datatype of com3: ", type(com3),'\n')

com3 = 0 + 56.47j
print(com3)
print("Datatype of com3: ", type(com3))
