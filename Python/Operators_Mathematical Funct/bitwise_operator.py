# BITWISE OPERATOR
# (-x-1)

x = 10; y = 11

print()

print("Bitwise Complement of x: ", ~x,"\n")

print("Bitwise AND of x and y: ", x & y,"\n")

print("Bitwise OR of x and y: ", x | y,"\n")

print("Bitwise XOR of x and y: ", x ^ y,"\n")

print("Bitwise Left Shift of x by 2 positions: ", x << 2,"\n")

print("Bitwise Right Shift of x by 2 positions: ", x >> 2,"\n")
